#include "libft.h"

char	*ft_strncat(char *dest, const char *src, int n)
{
	int			i;
	int			j;

	i = 0;
	while (dest[i] != '\0')
		i++;
	j = 0;
	while (j < n && src[j])
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
