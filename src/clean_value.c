#include "../includes/asm.h"

long long		clean_value(long long n)
{
	int		nu;

	if (n < -2147483648)
		nu = (2147483647 + (n + 2147483648)) + 1;
	else if (n > 2147483647)
		nu = ((n - 2147483647) + (-2147483648)) - 1;
	else
		nu = n;
	if (nu >= -2147483648 && nu < 0)
		n = 2147483647 + (nu + 2147483648) + 1;
	else
		n = nu;
	return (n);
}
